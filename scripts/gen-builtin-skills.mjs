// Embeds skills/*/SKILL.md into a TS module so built-in skills work inside
// compiled single-file binaries, where the package's skills/ dir doesn't exist.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const skillsDir = path.join(root, "skills");
const outFile = path.join(root, "src", "skills", "builtin.generated.ts");

const skills = [];
for (const entry of fs.readdirSync(skillsDir, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue;
  const file = path.join(skillsDir, entry.name, "SKILL.md");
  if (!fs.existsSync(file)) continue;
  const raw = fs.readFileSync(file, "utf8");
  const match = /^---\n([\s\S]*?)\n---\n?([\s\S]*)$/.exec(raw);
  if (!match) continue;
  const name = /name:\s*(.+)/.exec(match[1])?.[1]?.trim() ?? entry.name;
  const description = /description:\s*(.+)/.exec(match[1])?.[1]?.trim() ?? "";
  skills.push({ name, description, body: match[2].trim() });
}

skills.sort((a, b) => a.name.localeCompare(b.name));

const banner =
  "// AUTO-GENERATED by scripts/gen-builtin-skills.mjs — do not edit.\n" +
  "// Source of truth: skills/*/SKILL.md (run `npm run gen` after editing).\n";
const body =
  `export const BUILTIN_SKILLS: { name: string; description: string; body: string }[] = ` +
  JSON.stringify(skills, null, 2) +
  ";\n";

fs.writeFileSync(outFile, banner + body);
console.log(`Embedded ${skills.length} built-in skills → src/skills/builtin.generated.ts`);
